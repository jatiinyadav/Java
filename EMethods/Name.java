package EMethods;
public class Name {
    public static void main(String[] args) {
        String name = "Jatin Yadav";

        String changedName = changeName(name);
        System.out.println(changedName);
        System.out.println(name);
    }

    static String changeName(String name) {

        name = "Kamal Dev";

        return name;
    }

}